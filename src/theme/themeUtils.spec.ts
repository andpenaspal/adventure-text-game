import theme from '~/theme/theme';
import { getColor } from '~/theme/themeUtils';

describe('themeUtils', () => {
  test('getColor returns correct Color from Theme', () => {
    const expected = theme.palette.primary.light;
    const actual = getColor(theme, 'primary', 'light');
    expect(actual).toBe(expected);
  });

  test('getColor returns default Color Option from Theme', () => {
    const expected = theme.palette.primary.main;
    const actual = getColor(theme, 'primary');
    expect(actual).toBe(expected);
  });
});
